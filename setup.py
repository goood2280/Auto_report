def _decode():
    """Decode, decompress, verify checksum, and return the file map."""
    compressed = base64.b64decode("".join(_PAYLOAD_B64.split()))
    raw = gzip.decompress(compressed)
    actual = hashlib.sha256(raw).hexdigest()
    if actual != SHA256:
        sys.exit(
            "ERROR: checksum mismatch\n"
            "  expected: {}\n  actual:   {}".format(SHA256, actual)
        )
    return json.loads(raw.decode("utf-8"))


def main(argv=None):
    parser = argparse.ArgumentParser(
        description="Extract bundled Auto Report source files."
    )
    parser.add_argument(
        "--output", "-o", default=".",
        help="Target directory to extract into (default: current dir).",
    )
    parser.add_argument(
        "--overwrite", action="store_true",
        help="Overwrite files that already exist.",
    )
    parser.add_argument(
        "--list", action="store_true", dest="list_only",
        help="List bundled files and exit without extracting.",
    )
    args = parser.parse_args(argv)

    files = _decode()

    if args.list_only:
        for rel in sorted(files):
            size = len(files[rel].encode("utf-8"))
            print("{:>8}  {}".format(size, rel))
        print("\n{} file(s), sha256={}".format(len(files), SHA256))
        return 0

    written, skipped = 0, 0
    for rel in sorted(files):
        dest = os.path.join(args.output, *rel.split("/"))
        if os.path.exists(dest) and not args.overwrite:
            print("skip (exists): {}".format(rel))
            skipped += 1
            continue
        parent = os.path.dirname(dest)
        if parent:
            os.makedirs(parent, exist_ok=True)
        with open(dest, "w", encoding="utf-8", newline="") as f:
            f.write(files[rel])
        print("wrote: {}".format(rel))
        written += 1

    print("\nDone. {} written, {} skipped.".format(written, skipped))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
